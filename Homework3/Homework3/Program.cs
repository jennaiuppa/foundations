using System;


class LoopingArray
{
    static void Main()
    {
        Console.WriteLine("Enter a number");
        Console.WriteLine();
        Console.WriteLine();

        int n;
        int fact;


        for (n = -1; n >= 1; n--) ;
        for (fact = -1; fact>= 1; fact--) ;


        {
            int z = n*fact
                Console.Write("{0,6}", z);
        }
        Console.WriteLine();
    }
    Console.ReadLine();
}
